                    <h2>Overview</h2>
                    <p>The GSAPP faculty recognizes the impact of evolving health care delivery systems and other social and professional changes on the practice of professional psychology and continuously reviews its training goals and curricula. The combination of full-time core faculty and part-time faculty, who are professionals working in the community, enables GSAPP to offer both rigorous theoretical courses bases on current research and practice-oriented courses that build upon this scholarly foundation.</p>

<p>There are 20 full-time scholarly/instructional faculty; 6 visiting faculty (practitioners who spend one full day per week at the school teaching, supervising, and serving on academic and administrative committees); and approximately 26 contributing faculty (practicing psychologists who teach one course). Also, 45 joint appointment faculty from other Rutgers departments, centers, bureaus, and institutes, and from the University of Medicine and Dentistry of New Jersey's medical school actively teach and superviise. Students have access to relevant full-time faculty from all of these units for individual study,dissertation committees, and other training activities.</p>
<p>The GSAPP faculty participated in the pivotal 1978 Virginia Beach Conference on the training of professional psychologists, which enunciated the principle that professional training is best accomplished in a university setting.</p>
